# 1. NumPy
# NumPy (Numerical Python) is a library for numerical computing in Python. 
# It provid support for large, multi-dimensional arrays and matrices, along with a collection of mathematical
# functions.It's widely used in data science, machine learning, and scientific computing.

# 2. Pandas   
# Pandas is a powerful data manipulation and analysis library.
# It provides easy-to-use data structures like Series and DataFrame for handling structured data.
# It’s ideal for cleaning,  transforming, and analyzing tabular data.

# 3. SciPy
# SciPy builds on NumPy and provides additional tools for scientific and technical computing. 
# It includes modules for optimization, integration, signal processing, linear algebra, and statistics. 
# It’s used extensively in scientific research.

# 4. Matplotlib
# Matplotlib is a plotting library for creating static, animated, and interactive visualizations in Python. 
# It supports various chart types like line, bar, scatter, and histograms. It’s highly customizable and integrates
#  well with NumPy and Pandas.

# 5. Pygame
# Pygame is a library used to 2D develop video games and multimedia applications. 
# It provides modules for graphics, sound, and input handling, built on top of SDL (Simple DirectMedia Layer).
#  It’s great for learning game development basics.

# 6. PyQt
# PyQt is a set of Python bindings for the Qt application framework. 
# It enables building cross-platform desktop applications with modern GUIs. 
# It supports widgets, layouts, signals/slots, and integration with tools like Qt Designer.

# 7. BeautifulSoup (bs4)
# BeautifulSoup is a Python library used for web scraping purposes to extract data from HTML and XML documents.
#  It creates a parse tree for easy data extraction and works well with libraries like requests.